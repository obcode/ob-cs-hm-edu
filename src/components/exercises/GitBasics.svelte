    <p>
      Wenn Sie noch nicht mit Git gearbeitet haben, ist
      <a href="https://try.github.io/">https://try.github.io/</a>
      eine gute Quelle um Git kennen zu lernen.
    </p>
    <h4>Beachten Sie Folgendes beim Arbeiten mit git:</h4>
    <ul>
      <li>
        <p>
          Verwenden Sie lokal beim Committen eine
          <strong>E-Mail-Adresse</strong>
          , die Sie auch in GitHub hinzugefügt haben. Anderenfalls kann GitHub
          Ihnen Ihre Commits nicht zuordnen, was insbesondere bei Teams ein
          Problem darstellt. Dadurch gewinne ich den Eindruck Sie haben gar
          nichts beigetragen!
        </p>
        <p>
          Infos, wie Sie Ihre E-Mail-Adresse lokal setzen, finden Sie unter
          <a
            href="https://git-scm.com/book/en/v2/Getting-Started-First-Time-Git-Setup">
            Getting Started - First-Time Git Setup
          </a>
          .
        </p>
      </li>
      <li>
        <p>
          <strong>Committen</strong>
          Sie kleine sinnvolle Einheiten mit
          <strong>vernünftigen</strong>
          Commit-Messages. Eine Commit-Message sollte kurz beschreiben, was im
          Gegensatz zum letzten Commit verändert wurde, z.B.
        </p>
        <ul>
          <li>“Erster Ansatz der Klasse Blablabla.”</li>
          <li>“Ausgabe für rationale Zahlen hinzugefügt.”</li>
          <li>“Funktion hallo gefixt.”</li>
          <li>“Lösung für Teilaufgabe 2b”</li>
          <li>…</li>
        </ul>
      </li>
      <li>
        <p>
          <strong>Pushen</strong>
          Sie nach jedem Commit. Sie haben damit sofort ein Backup ihres
          aktuellen Stands auf GitHub. Wenn ein CI-Job für Sie eingerichtet ist,
          bekommen Sie sofort eine Rückmeldung. Wenn Sie im Team arbeiten,
          können die anderen Team-Mitglieder sofort sehen, was Sie gemacht haben
          und evtl. darauf reagieren.
        </p>
      </li>
    </ul>
  </div>
</div>
