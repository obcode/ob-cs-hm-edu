    <h3>Frage &amp; Hilfe zu Ihrem Code</h3>
    <p>
      Sollten Sie eine Frage zu Ihrem Code haben oder konkrete Hilfe benötigen,
      erzeugen Sie in dem entsprechenden Repository über die GitHub-Seite des
      Repository ein
      <a href="https://help.github.com/articles/about-issues/">Issue</a>
      . In diesem Issue beschreiben Sie Ihr Problem und verlinken auf
      zusätzliche Informationen, wie den Code oder Drone CI/GitHub Actions-Job.
      Nutzen Sie
      <a href="https://guides.github.com/features/mastering-markdown/">
        Markdown
      </a>
      um Ihr Issue besser lesbar zu gestalten. Ein gut lesbares Issue bearbeite
      ich viel lieber und schneller!
    </p>
    <h4>Und jetzt das wichtigste:</h4>
    <p>
      Damit ich (oder, falls vorhanden, ein Tutor) überhaupt mit bekomme, dass
      Sie eine Frage haben, führen Sie folgende Schritte durch:
    </p>
    <ul>
      <li>
        weisen Sie es (Ihrem Tutor und) mir zu
        <em>(Assignees)</em>
        und
      </li>
      <li>
        setzen Sie das Label
        <strong>help wanted</strong>
        oder
        <strong>question</strong>
      </li>
    </ul>
    <p>
      Ohne diese beiden Schritte werden die Issues von uns
      <strong>nicht registriert</strong>
      und daher
      <strong>nicht bearbeitet!</strong>
      Durch die Zuweisung werden wir per E-Mail informiert und können,
      schnellstmöglich, auf Ihre Frage reagieren.
    </p>
    <h3>Mattermost</h3>
    <p>
      Zur Kommunikation bezüglich einer Lehrveranstaltung von mir gibt es
      jeweils einen
      <a href="https://mattermost.com/">Mattermost</a>
      -Room. Sie finden ihn auf der Seite zur Veranstaltung unter
      <em>Links</em>
      .
    </p>
    <p>
      Stellen Sie in diesem Raum inbesondere alle Fragen, die nicht nur Sie
      interessieren könnten. Wer weiter helfen kann, darf gerne weiter helfen.
      Mich selbst finden Sie in den Räumen jeweils unter dem Namen obcode.
    </p>
    <p>
      Nutzen Sie auch in Gitter die Möglichkeit Ihre Nachricht in
      <a href="https://guides.github.com/features/mastering-markdown/">
        Markdown
      </a>
      zu formatieren. Damit werden insbesondere Code-Schnipsel viel besser
      lesbar.
    </p>
